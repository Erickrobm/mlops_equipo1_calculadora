def resta(a,b):
    """

    Parameters
    ----------
    a : int, float, string.
    Primer valor definido para la operación.
        
    b : int, float, string.
    Segundo valor definifo para la operación.  

    Returns
    -------
    Valor obtenido por la resta de los parámetros.

    """
    minuendo = float(a)
    sustraendo = float(b)
    return minuendo - sustraendo
